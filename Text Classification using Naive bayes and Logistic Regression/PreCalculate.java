
public class PreCalculate {
	private int spamSum;
	private int hamSum;

	public PreCalculate(int spamSum, int hamSum) {
		this.spamSum = spamSum;
		this.hamSum = hamSum;
	}

	public int getSpamSum() {
		return spamSum;
	}

	public void setSpamSum(int spamSum) {
		this.spamSum = spamSum;
	}

	public int getHamCount() {
		return hamSum;
	}

	public void setHamCount(int hamSum) {
		this.hamSum = hamSum;
	}

}